#include <unistd.h>

int		ft_sqrt(int nb)
{
	int i;

	i = 2;
	if (nb > 2147395600)
		return (0);
	while (i <= (nb / 2))
	{
		if ((i * i) == nb)
			return (i);
		++i;
	}
	return (0);
}
